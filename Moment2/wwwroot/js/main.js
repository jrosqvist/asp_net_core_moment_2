// Öppna mobilmenyn
function openMobileMenu() {
    document.getElementById("mobile-menu-container").style.width = "100%";
    document.getElementById("mobile-menu-button").style.display = "none";
}
// Stäng mobilmenyn
function closeMobileMenu() {
    document.getElementById("mobile-menu-container").style.width = "0";
    document.getElementById("mobile-menu-button").style.display = "block";
}